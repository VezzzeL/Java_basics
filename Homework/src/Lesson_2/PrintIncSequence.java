package Lesson_2;

public class PrintIncSequence {
    public static void main(String[] args) {
        int size=14;
        for(int i = 1;i<=size;i++){
            if(i<size){
                System.out.print((7*i)+" ");
            }else{
                System.out.print(7*i);
            }
        }
    }
}
